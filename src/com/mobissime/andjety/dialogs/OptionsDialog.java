    public void WriteOptions() {
        WriteOptionsXML saveOptions = new WriteOptionsXML();
        String updateStr = "";
        String updateStrAstuce = "";

        if (jCheckBoxMAJ.isSelected()) {
            updateStr = "oui";
        } else {
            updateStr = "non";
        }

        if (jCheckBoxMAJAstuce.isSelected()) {
            updateStrAstuce = "oui";
        } else {
            updateStrAstuce = "non";
        }

        saveOptions.WriteOptionsFile(jTextFieldMysql_user.getText(), jTextFieldMysql_password.getText(), jTextFieldMysql_serveur.getText(), jComboBox1.getSelectedItem().toString(), updateStr, updateStrAstuce, jComboBox2.getSelectedItem().toString());
        this.hide();
    }

