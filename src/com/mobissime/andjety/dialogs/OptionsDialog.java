    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Si la langue à changée alors on alerte qu'il faut redémarrer Andjety

        OptionsParser optionsParse = new OptionsParser();
        optionsParse.parseXml();
        if (!jComboBox2.getSelectedItem().toString().equals(optionsParse.languageValue)) {
            JOptionPane.showMessageDialog(this, "Veuillez quitter et relancer Andjety afin que le changement de langue soit opéré.", "Andjety", JOptionPane.INFORMATION_MESSAGE);
        }
        WriteOptions();
    }//GEN-LAST:event_jButton1ActionPerformed

